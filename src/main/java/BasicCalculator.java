
public class BasicCalculator {

    public static void main(String[] args) {

        int first = 5;
        int second = 5;

        System.out.println(first + " + " + second + " = " + (first + second));
        System.out.println(first + " - " + second + " = " + (first - second));
        System.out.println(first + " * " + second + " = " + (first * second));
        System.out.println(first + " / " + second + " = " + (1.0 * first / second));
    }
}
